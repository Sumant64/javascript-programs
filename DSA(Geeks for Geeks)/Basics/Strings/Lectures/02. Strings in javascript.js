// Strings in javascript

/**
 * Strings in Javascript:
 * ---------------------
 * o No character type
 * o Typically stored using UTF-16.
 * o We are either use single quote or double quote.
 * 
 */

let s1 = "gfg";
let s2 = "geeksforgeeks";
console.log(s1);
console.log(s2);

